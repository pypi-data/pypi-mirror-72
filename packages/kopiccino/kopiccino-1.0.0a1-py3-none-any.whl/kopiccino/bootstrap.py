"""Bootstrap utilites for kopiccino.

If kopiccino was not installed through Pip, kopiccino can install itself using its own
package. Packages generated by kopiccino can be importable as a namespace (by adding
the zip path to sys.path.) So, this is possible:

>>> import sys
>>> sys.path.insert(-1, "/path/to/kopiccino/zip")
>>> import kopiccino.concrete
>>> kopiccino.concrete.install("/path/to/kopiccino/zip")

Or shorter (this module does all of it for you):

>>> import kopiccino
>>> kopiccino.bootstrap()

Pip also bootstraps pretty much the same way.
Bootstrapping is only supported if kopiccino was not installed through Pip.
"""

