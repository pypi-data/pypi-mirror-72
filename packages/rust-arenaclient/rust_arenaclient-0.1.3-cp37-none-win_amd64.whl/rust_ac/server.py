from . import PServer
from multiprocessing import Process


class Server:
    """
    Set up and run the Proxy server.
    """
    def __init__(self, ip_addr: str):
        self.server = PServer(ip_addr)
        self.process: Process = None

    def run(self):
        self.process = Process(target=self.server.run)
        self.process.start()

    def kill(self):
        self.process.kill()
